#include "Tracklet.h"
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//constructors and destructors

Tracklet::Tracklet(TrlRegion & reg){
  region_ = reg;
  trk_    = new TrackingKalman;
  TrkPoint centroid(region_.x + region_.width , region_.y + region_.height);
  trk_->newTrack(centroid);
}

Tracklet::~Tracklet(){
  if (trk_) delete trk_;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//main functions 

///feed function feeds the tracklet with new observation
///in: region of new observation
void Tracklet::find_next(TrlRegion & reg){
  region_ = reg;
  trk_->predict();
}

